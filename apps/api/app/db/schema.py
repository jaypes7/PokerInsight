    Column("timezone_at_play", String(40)),
    Column("board_cards", ARRAY(String(2)), nullable=False),
    Column("pot_total", BigInteger, nullable=False),
    Column("rake", BigInteger, nullable=False),
    Column("hero_position", String(8)),
    Column("hero_starting_stack", BigInteger),
    Column("hero_hole_cards", ARRAY(String(2))),
    Column("hero_net_cents", BigInteger),
