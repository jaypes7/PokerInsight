    Column("buy_in_cents", BigInteger),
    Column("fee_cents", BigInteger),
    Column("currency", CHAR(3), nullable=False),
    Column("level_name", String(20)),
    Column("small_blind", BigInteger, nullable=False),
    Column("big_blind", BigInteger, nullable=False),
    Column("ante", BigInteger, nullable=False),
    Column("button_seat", SmallInteger, nullable=False),
    Column("hero_seat", SmallInteger),
    Column("hero_username", String(60), nullable=False),
