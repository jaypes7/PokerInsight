from sqlalchemy import (
    CHAR,
    BigInteger,
    Boolean,
    Column,
    DateTime,
    ForeignKey,
    Integer,
    MetaData,
    SmallInteger,
    String,
    Table,
    Text,
    text,
)
from sqlalchemy.dialects.postgresql import ARRAY, INET, UUID

from app.db.base import NAMING_CONVENTION

metadata = MetaData(naming_convention=NAMING_CONVENTION)

users = Table(
    "users",
    metadata,
    Column("id", UUID(as_uuid=True), primary_key=True, server_default=text("uuid_generate_v7()")),
    Column("email", String, nullable=False),
    Column("email_verified_at", DateTime(timezone=True)),
    Column("display_name", String(80), nullable=False),
    Column("password_hash", String(255)),
    Column("role", String(20), nullable=False),
    Column("locale", String(10), nullable=False),
    Column("timezone", String(64), nullable=False),
    Column("created_at", DateTime(timezone=True), nullable=False),
    Column("updated_at", DateTime(timezone=True), nullable=False),
    Column("deleted_at", DateTime(timezone=True)),
    Column("last_login_at", DateTime(timezone=True)),
)

refresh_tokens = Table(
    "refresh_tokens",
    metadata,
    Column("id", UUID(as_uuid=True), primary_key=True, server_default=text("uuid_generate_v7()")),
    Column("user_id", UUID(as_uuid=True), ForeignKey("users.id"), nullable=False),
    Column("token_hash", CHAR(64), nullable=False),
    Column("family_id", UUID(as_uuid=True), nullable=False),
    Column("parent_id", UUID(as_uuid=True), ForeignKey("refresh_tokens.id")),
    Column("user_agent", String(255)),
    Column("ip_address", INET),
    Column("issued_at", DateTime(timezone=True), nullable=False),
    Column("expires_at", DateTime(timezone=True), nullable=False),
    Column("revoked_at", DateTime(timezone=True)),
    Column("used_at", DateTime(timezone=True)),
)

imports = Table(
    "imports",
    metadata,
    Column("id", UUID(as_uuid=True), primary_key=True, server_default=text("uuid_generate_v7()")),
    Column("user_id", UUID(as_uuid=True), ForeignKey("users.id"), nullable=False),
    Column("original_filename", String(255), nullable=False),
    Column("file_size_bytes", BigInteger, nullable=False),
    Column("file_hash", CHAR(64), nullable=False),
    Column("storage_key", String(500), nullable=False),
    Column("source", String(40), nullable=False),
    Column("language", String(10), nullable=False),
    Column("status", String(20), nullable=False),
    Column("total_hands_detected", Integer),
    Column("total_hands_imported", Integer),
    Column("total_hands_duplicate", Integer),
    Column("total_errors", Integer),
    Column("started_at", DateTime(timezone=True)),
    Column("finished_at", DateTime(timezone=True)),
    Column("error_message", Text),
    Column("created_at", DateTime(timezone=True), nullable=False),
)

import_errors = Table(
    "import_errors",
    metadata,
    Column("id", UUID(as_uuid=True), primary_key=True, server_default=text("uuid_generate_v7()")),
    Column("import_id", UUID(as_uuid=True), ForeignKey("imports.id"), nullable=False),
    Column("line_start", Integer, nullable=False),
    Column("line_end", Integer, nullable=False),
    Column("raw_excerpt", Text, nullable=False),
    Column("error_code", String(60), nullable=False),
    Column("error_message", Text, nullable=False),
    Column("created_at", DateTime(timezone=True), nullable=False),
)

sessions = Table(
    "sessions",
    metadata,
    Column("id", UUID(as_uuid=True), primary_key=True, server_default=text("uuid_generate_v7()")),
    Column("user_id", UUID(as_uuid=True), ForeignKey("users.id"), nullable=False),
    Column("started_at", DateTime(timezone=True), nullable=False),
    Column("ended_at", DateTime(timezone=True), nullable=False),
    Column("total_hands", Integer, nullable=False),
    Column("net_profit_cents", BigInteger, nullable=False),
    Column("currency", CHAR(3), nullable=False),
    Column("created_at", DateTime(timezone=True), nullable=False),
)

